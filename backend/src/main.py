@app.post("/autoscale/apply", tags=["Autoscale"])
def manual_scale_trigger(request: ScaleRequest):
    """Overrides the automated scaling engine logic for emergency capacity management."""
    logger.warning(f"MANUAL SCALE for {request.pool_id} to {request.target_host_count} hosts.")
    return {"message": f"Scaling sequence for {request.pool_id} initiated."}

